    @Override
    public void modelView(Model m) {
        Aluno a = (Aluno) m;
        this.jAluno.setText(a.getAluno());
        this.jNumero.setText(a.getNumero());
        this.jTurma.setText(a.getTurma());
    }

    @Override
    public Model viewModel() {
        String aluno, numero, turma;
        aluno = this.jAluno.getText();
        numero = this.jNumero.getText();
        turma = this.jTurma.getText();
        
        return new Aluno(aluno, numero, turma);
    }

    @Override
    public void clearView() {
        this.jAluno.setText("");
        this.jNumero.setText("");
        this.jTurma.setText("");
    }
